<template>
  <div class="layout-header">
    <!--  左侧菜单  -->
    <div class="layout-header-left">
      <!-- 菜单收起 -->
      <div
        class="ml-1 layout-header-trigger layout-header-trigger-min"
        @click="() => $emit('update:collapsed', !collapsed)"
      >
        <NIcon size="18" v-if="collapsed">
          <MenuUnfoldOutlined />
        </NIcon>
        <NIcon size="18" v-else>
          <MenuFoldOutlined />
        </NIcon>
      </div>
      <!-- 刷新 -->
