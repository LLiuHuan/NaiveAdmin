    <!--  右侧菜单  -->
    <div class="layout-header-right">
      <!--   动态图标按钮   -->
      <div
        class="layout-header-trigger layout-header-trigger-min"
        v-for="item in iconList"
        :key="item.icon.name"
      >
        <n-tooltip placement="bottom">
          <template #trigger>
            <n-icon size="18">
              <component :is="item.icon" v-on="item.eventObject || {}" />
            </n-icon>
          </template>
          <span>{{ item.tips }}</span>
        </n-tooltip>
      </div>
      <!--切换全屏-->
      <div class="layout-header-trigger layout-header-trigger-min">
        <NTooltip placement="bottom">
          <template #trigger>
            <NIcon size="18">
              <component :is="fullscreenIcon" @click="toggleFullScreen" />
            </NIcon>
          </template>
          <span>全屏</span>
        </NTooltip>
      </div>
      <!-- 个人中心 -->
      <div class="layout-header-trigger layout-header-trigger-min">
        <n-dropdown
          trigger="hover"
          @select="avatarSelect"
          :options="avatarOptions"
        >
          <div class="avatar">
            <n-avatar round>
              admin
              <template #icon>
                <UserOutlined />
              </template>
            </n-avatar>
          </div>
        </n-dropdown>
      </div>
    </div>
  </div>
</template>

<script lang="ts">
import { computed, defineComponent, reactive, toRefs, unref } from "vue";
import components from "./components";
import { useStore } from "vuex";
import { RouteLocationMatched, useRoute, useRouter } from "vue-router";
import { useDialog, useMessage } from "naive-ui";

export default defineComponent({
  name: "PageHeader",
  components: {
    ...components,
  },
  props: {
    collapsed: {
      type: Boolean,
    },
    inverted: {
      type: Boolean,
    },
  },
  emits: ["update:collapsed"],
  setup() {
    const store = useStore();
    const router = useRouter();
    const route = useRoute();
    const dialog = useDialog();
    const message = useMessage();

    const state = reactive({
      fullscreenIcon: "FullscreenOutlined",
      iconList: [
        {
          icon: "GithubAlt",
          tips: "github",
          eventObject: {
            click: () => window.open("https://github.com/LLiuHuan/NaiveAdmin"),
          },
        },
      ],
      avatarOptions: [
        {
          label: "个人设置",
          key: 1,
        },
        {
          type: "divider",
          key: "d1",
        },
        {
          label: "退出登录",
          key: 2,
        },
      ],
    });

    // header 变量
    const headerSetting = store.getters.getHeaderSetting;
    const crumbsSetting = store.getters.getCrumbsSetting;

    // 切换全屏图标
    const toggleFullscreenIcon = () =>
      (state.fullscreenIcon =
        document.fullscreenElement !== null
          ? "FullscreenExitOutlined"
          : "FullscreenOutlined");

    // 监听全屏切换事件
    document.addEventListener("fullscreenchange", toggleFullscreenIcon);

    // 全屏切换
    const toggleFullScreen = () => {
      if (!document.fullscreenElement) {
        document.documentElement.requestFullscreen();
      } else {
        if (document.exitFullscreen) {
          document.exitFullscreen();
        }
      }
    };

    // 刷新页面
    const reloadPage = () => {
      router.push({
        path: "/redirect" + unref(route).fullPath,
      });
    };

    //头像下拉菜单
    const avatarSelect = (key: number) => {
      switch (key) {
        case 1:
          router.push({ name: "Setting" });
          break;
        case 2:
          doLogout();
          break;
      }
    };

    // 退出登录
    const doLogout = () => {
      dialog.info({
        title: "提示",
        content: "您确定要退出登录吗",
        positiveText: "确定",
        negativeText: "取消",
        onPositiveClick: () => {
          message.success("成功退出登录");
        },
        onNegativeClick: () => {},
      });
    };

    const generator: any = (routerMap: RouteLocationMatched[]) => {
      return routerMap.map((item) => {
        const currentMenu = {
          ...item,
          label: item.meta.title,
          key: item.name,
          disabled: item.path === "/",
        };
        // 是否有子菜单，并递归处理
        if (item.children && item.children.length > 0) {
          // Recursion
          currentMenu.children = generator(item.children, currentMenu);
        }
        return currentMenu;
      });
    };

    // 面包屑
    const breadcrumbList = computed(() => {
      return generator(route.matched);
    });

    const dropdownSelect = (key: any) => {
      router.push({ name: key });
    };

    return {
      ...toRefs(state),
      headerSetting,
      crumbsSetting,
      toggleFullScreen,

      reloadPage,
      avatarSelect,
      breadcrumbList,
      dropdownSelect,
    };
  },
});
</script>

